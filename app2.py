def extract_student_id(text):
    """‡∏î‡∏∂‡∏á‡πÄ‡∏•‡∏Ç‡∏ó‡∏µ‡πà‡∏à‡∏≤‡∏Å‡∏Ç‡πâ‡∏≠‡∏Ñ‡∏ß‡∏≤‡∏° ‡πÄ‡∏ä‡πà‡∏ô '‡πÄ‡∏•‡∏Ç‡∏ó‡∏µ‡πà 7' ‡∏´‡∏£‡∏∑‡∏≠ '‡πÄ‡∏•‡∏Ç‡∏ó‡∏µ‡πà7'"""
